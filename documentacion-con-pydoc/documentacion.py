def area(r):
	"""
	Funcion que calcula el area de un circulo de radio r
	r - Es el radio del circulo

	"""

	area = 3.141592653 * (r**2)

	return area

def perimetro(d):
	"""
	Funcion que calcula el perimetro de un circulo de diametro d
	d - Es el diametro del circulo

	"""

	perimetro = 3.141592653 * d

	return perimetro