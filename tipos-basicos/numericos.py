# Con enteros 
x = 2 * 3 + 1 - 5 
print(x)

x = 5 / 2
print(x)

x = 5 % 2
print(x)

x = 5 ** 2
print(x)


# Con flotantes
x = 2.1 * 3.2 + 1.5 - 5.3
print(x)

x = 5.0 / 2
print(x)

x = 5 / 2.0
print(x)

x = 5.3 % 2.1
print(x)

x = 4.5 ** 2.3
print(x)


# Con complejos
